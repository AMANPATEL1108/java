package Course.w3resourceExercise.JavaBasic.BasicExcercisePart1;

/**
 * Writeaprogramtocomparetwonumbers
 */
public class Writeaprogramtocomparetwonumbers {

    public static void main(String[] args) {
        int a = 25;
        int b = 39;

        if (a == b) {
            System.out.println(a + "==" + b);

        }

        if (a != b) {
            System.out.println(a + "!=" + b);

        }
        if (a < b) {
            System.out.println(a + "<" + b);

        }
        if (a > b) {
            System.out.println(a + ">" + b);

        }
        if (a <= b) {
            System.out.println(a + "<=" + b);

        }
        if (a >= b) {
            System.out.println(a + ">=" + b);

        }

    }
}
