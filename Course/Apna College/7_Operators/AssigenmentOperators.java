public class AssigenmentOperators {

    public static void main(String[] args) {
        int e = 10;
        int a = 10;
        int b = 5;
        int d = 12;
        int f = 15;

        e = b;
        System.out.println(e);// now e is 5

        a += 2;
        System.out.println(a); // now a is 12

        b -= 2;
        System.out.println(b);// now b is 3

        d *= 2;
        System.out.println(d);// now d is 24

        f /= 3;
        System.out.println(f);// now f is 5

    }
}
