import java.util.Scanner;

public class Multi {
    public static void main(String[] args) {
        Scanner Sc = new Scanner(System.in);

        System.out.println("Enter a Number 1");
        int a = Sc.nextInt();
        System.out.println("Enter a Number 2");
        int b = Sc.nextInt();

        System.out.println(a * b);

    }
}